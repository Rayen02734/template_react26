export const PURCHASED_COURSES_KEY = 'growup-purchased-courses';

export function getPurchasedCourses() {
  if (typeof window === 'undefined') {
    return [];
  }

  try {
    const stored = window.localStorage.getItem(PURCHASED_COURSES_KEY);
    return stored ? JSON.parse(stored) : [];
  } catch {
    return [];
  }
}

export function savePurchasedCourses(courseIds) {
  if (typeof window === 'undefined') {
    return;
  }

  window.localStorage.setItem(PURCHASED_COURSES_KEY, JSON.stringify(courseIds));
}
